    DefaultTableModel tabla = new DefaultTableModel();

    public void creartabla() {
        tabla.addColumn("IDENTIFICACION");
        tabla.addColumn("NOMBRE");
        tabla.addColumn("APELLIDO");
        tabla.addColumn("TELEFONO");
    }

    public void llenartabla() {
        tabla.setNumRows(0);
        for (Dueño d : vistaInicial.ctrldueño.getDueños()) {
            String[] fila = {d.getIdentificacion(), d.getNombre().toUpperCase(), d.getApellido().toUpperCase(), d.getTelefono()};
            tabla.addRow(fila);
        }
    }

    public void cargarTabla() {
        if (vistaInicial.ctrldueño.validarArchivo() == true) {
            tabla.setNumRows(0);
            for (Dueño d : vistaInicial.ctrldueño.cargarDueños()) {
                String[] fila = {d.getIdentificacion(), d.getNombre().toUpperCase(), d.getApellido().toUpperCase(), d.getTelefono()};
                tabla.addRow(fila);
                vistaInicial.ctrldueño.agregar(d);
            }
        }
    }

    public void mostrarConsulta() {
        for (int i = 0; i < tabla.getRowCount(); i++) {
            tabla.removeRow(i);
        }
        tabla.setNumRows(0);
        for (Dueño d : vistaInicial.ctrldueño.getDueños()) {
            if (d.getIdentificacion().equals(identificacion.getText())) {
                String[] fila = {d.getIdentificacion(), d.getNombre().toUpperCase(), d.getApellido().toUpperCase(), d.getTelefono()};
                tabla.addRow(fila);
                nombre.setText(d.getNombre());
                apellido.setText(d.getApellido());
                codigoPostal.setText(d.getCodigoPostal());
                telefono.setText(d.getTelefono());
                email.setText(d.getEmail());
                nombre.setEnabled(true);
                apellido.setEnabled(true);
                jRadioButtonMas.setEnabled(true);
                jRadioButtonFem.setEnabled(true);
            }
        }
    }

    public void mostrarDueñoActualizar() {
        for (int i = 0; i < tabla.getRowCount(); i++) {
            tabla.removeRow(i);
        }
        tabla.setNumRows(0);
        for (Dueño d : vistaInicial.ctrldueño.getDueños()) {
            if (d.getIdentificacion().equals(identificacion.getText())) {
                String[] fila = {d.getIdentificacion(), d.getNombre().toUpperCase(), d.getApellido().toUpperCase(), d.getTelefono()};
                tabla.addRow(fila);
                nombre.setText(d.getNombre());
                apellido.setText(d.getApellido());
            }
        }
    }

