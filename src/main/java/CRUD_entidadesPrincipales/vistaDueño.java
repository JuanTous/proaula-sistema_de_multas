    public String sexo() {
        String validar = "";
        if (jRadioButtonMas.isSelected()) {
            validar = "MASCULINO";
        }
        if (jRadioButtonFem.isSelected()) {
            validar = "FEMENINO";
        }
        return validar;
    }

    public void btnAñadir() {
        if (identificacion.getText().equals("") || nombre.getText().equals("")
                || apellido.getText().equals("") || codigoPostal.getText().equals("")
                || telefono.getText().equals("") || email.getText().equals("")
                || sexo().equals("")) {
            JOptionPane.showMessageDialog(null, "¡FALTAN CAMPOS POR LLENAR!\n"
                    + "Por favor, complete todos los campos.", "Campos incompletos", JOptionPane.ERROR_MESSAGE);
        } else {
            if (vistaInicial.ctrldueño.verificarYAgregar(identificacion.getText()) == true) {
                JOptionPane.showMessageDialog(null, "Hay una persona con la misma identidicación, digite la correcta por favor.",
                        "Agregar dueño", JOptionPane.ERROR_MESSAGE);
                identificacion.setText("");
            } else {
                if (identificacion.getText().length() != 10 || telefono.getText().length() != 10 || codigoPostal.getText().length() != 6) {
                    JOptionPane.showMessageDialog(null, "Por favor, ingrese los digitos que se requieren en el campo IDENTIFICACION, TELEFONO o CODIGO POSTAL.", "Campos incompletos", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    Dueño constructor = new Dueño();
                    constructor.setIdentificacion(identificacion.getText());
                    constructor.setNombre(nombre.getText());
                    constructor.setApellido(apellido.getText());
                    constructor.setSexo(sexo());
                    constructor.setCodigoPostal(codigoPostal.getText());
                    constructor.setTelefono(telefono.getText());
                    constructor.setEmail(email.getText());
                    constructor.setEstado("ACTIVO");
                    constructor.setUsuario(constructor.getIdentificacion());
                    constructor.setContraseña(constructor.getIdentificacion());
                    vistaInicial.ctrldueño.agregar(constructor);
                    vaciar();
                    llenartabla();
                }
            }
        }
    }

    public void btnConsultar() {
        if (vistaInicial.ctrldueño.listaVacia() == true) {
            JOptionPane.showMessageDialog(null, "No se ha registrado ningun propietario.", "Consultar", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if (identificacion.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Ingrese la identificacion de la persona a buscar", "Campos incompletos", JOptionPane.ERROR_MESSAGE);
            } else {
                vistaInicial.ctrldueño.consultar(identificacion.getText());
                mostrarConsulta();
                Atras.setVisible(true);
                Añadir.setEnabled(false);
                Guardar.setEnabled(false);
                Modificar.setEnabled(false);
            }
        }
    }

    public void btnActualizar() {
        if (vistaInicial.ctrldueño.listaVacia() == true) {
            JOptionPane.showMessageDialog(null, "La lista se encuentra vacía.", "Actualizar", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if (identificacion.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Ingrese la identificacion de la persona a actualizar", "Campos incompletos", JOptionPane.ERROR_MESSAGE);
            } else {
                mostrarDueñoActualizar();
                nombre.setEnabled(false);
                apellido.setEnabled(false);
                jRadioButtonMas.setEnabled(false);
                jRadioButtonFem.setEnabled(false);
                if (codigoPostal.getText().equals("") || telefono.getText().equals("") || email.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos a actualizar", "Campos incompletos", JOptionPane.ERROR_MESSAGE);
                    Atras.setVisible(true);
                    Añadir.setEnabled(false);
                    Guardar.setEnabled(false);
                } else {
                    if (identificacion.getText().length() != 10 || telefono.getText().length() != 10 || codigoPostal.getText().length() != 6) {
                        JOptionPane.showMessageDialog(null, "Por favor, ingrese los digitos que se requieren en el campo IDENTIFICACION, TELEFONO o CODIGO POSTAL.", "Campos incompletos", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        vistaInicial.ctrldueño.actualizar(identificacion.getText(), codigoPostal.getText(), telefono.getText(), email.getText());
                        nombre.setEnabled(true);
                        apellido.setEnabled(true);
                        jRadioButtonMas.setEnabled(true);
                        jRadioButtonFem.setEnabled(true);
                        vaciar();
                        llenartabla();
                    }
                }
            }

        }
    }

    public void btnEliminar() {
        if (vistaInicial.ctrldueño.listaVacia() == true) {
            JOptionPane.showMessageDialog(null, "La lista se encuentra vacía.", "Eliminar", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if (identificacion.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Ingrese la identificacion de la persona a eliminar", "Campos incompletos", JOptionPane.ERROR_MESSAGE);
            } else {
                boolean validar = vistaInicial.ctrldueño.eliminar(identificacion.getText());
                if (validar == false) {
                    JOptionPane.showMessageDialog(null, "No existe alguna persona con esa identificación.", "Eliminar dueño", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    llenartabla();
                    vaciar();
                }
            }
        }
    }

