    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        if (departamento.getText().equals("") || municipio.getText().equals("") || barrio.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos", "Lugar", JOptionPane.INFORMATION_MESSAGE);
        } else {
            constructorInfraccion.datosDeVistaLugar = enviarDatos();
            constructor.setDepartamento(departamento.getText());
            constructor.setMunicipio(municipio.getText());
            constructor.setBarrio(barrio.getText());
            vistaInfraccion.ctrlLugar.agregar(constructor);
            vistaInfraccion.ctrlLugar.guardarLugar();
            this.dispose();
        }
    }//GEN-LAST:event_btnAsignarActionPerformed

